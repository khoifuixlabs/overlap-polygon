const { getBoundingBoxOfPolygon } = require('../getBoundingBoxOfPolygon');

describe('Test getBoundingBoxOfPolygon', () => {
  it('#1', () => {
    const poly = [
      [-91.88206347231792, 42.77278848732999],
      [-91.8829916484676, 42.772135308023394],
      [-91.8817719789813, 42.77135824090999],
      [-91.88026081615922, 42.77215220067873],
      [-91.88071339791814, 42.77314322837765],
      [-91.88206347231792, 42.77278848732999],
    ];
    const boundingBox = getBoundingBoxOfPolygon(poly);
    expect(boundingBox).toEqual([
      [-91.8829916484676, 42.77135824090999],
      [-91.88026081615922, 42.77314322837765],
    ]);
  });
  it('#2', () => {
    const poly = [
      [-91.88118132143187, 42.771938226707334],
      [-91.88186402950858, 42.77311507440061],
      [-91.88413460918062, 42.771938226707334],
      [-91.88013041124572, 42.77160600249775],
      [-91.88144213125886, 42.77319953629339],
      [-91.88118132143187, 42.771938226707334],
    ];
    const boundingBox = getBoundingBoxOfPolygon(poly);
    expect(boundingBox).toEqual([
      [-91.88413460918062, 42.77160600249775],
      [-91.88013041124572, 42.77319953629339],
    ]);
  });
  it('#3', () => {
    const poly = [
      [-91.88003220643327, 42.77286672651931],
      [-91.88280641861276, 42.773228567748845],
      [-91.88459475373664, 42.771520321981626],
      [-91.88120150247562, 42.77057782138982],
      [-91.88003220643327, 42.77286672651931],
    ];
    const boundingBox = getBoundingBoxOfPolygon(poly);
    expect(boundingBox).toEqual([
      [-91.88459475373664, 42.77057782138982],
      [-91.88003220643327, 42.773228567748845],
    ]);
  });
});
